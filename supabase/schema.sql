create table public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  display_name text not null check (char_length(display_name) between 2 and 40),
  is_admin boolean not null default false,
  created_at timestamptz not null default now()
);

create table public.matches (
  id bigint generated by default as identity primary key,
  match_number smallint not null unique check (match_number between 1 and 104),
  home_team text not null,
  away_team text not null,
  home_flag text not null default 'вљЅ',
  away_flag text not null default 'вљЅ',
  stage text not null,
  kickoff_at timestamptz not null,
  venue text,
  status text not null default 'scheduled'
    check (status in ('scheduled', 'finished')),
  home_score smallint check (home_score between 0 and 99),
  away_score smallint check (away_score between 0 and 99),
  created_at timestamptz not null default now(),
  constraint finished_match_has_score check (
    status <> 'finished' or (home_score is not null and away_score is not null)
  )
);

create table public.predictions (
  user_id uuid not null references public.profiles(id) on delete cascade,
  match_id bigint not null references public.matches(id) on delete cascade,
  home_score smallint not null check (home_score between 0 and 99),
  away_score smallint not null check (away_score between 0 and 99),
  points smallint check (points between 0 and 7),
  updated_at timestamptz not null default now(),
  primary key (user_id, match_id)
);

create index predictions_match_id_idx on public.predictions(match_id);
create index matches_kickoff_at_idx on public.matches(kickoff_at);
create unique index profiles_username_idx
  on public.profiles (
    regexp_replace(lower(trim(display_name)), '[[:space:]]+', '_', 'g')
  );

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer set search_path = ''
as $$
begin
  insert into public.profiles (id, display_name)
  values (
    new.id,
    coalesce(nullif(trim(new.raw_user_meta_data ->> 'display_name'), ''), 'Participante')
  );
  return new;
end;
$$;

create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

create or replace function public.is_admin()
returns boolean
language sql
stable
security definer set search_path = ''
as $$
  select coalesce(
    (select is_admin from public.profiles where id = auth.uid()),
    false
  );
$$;

create or replace function public.update_username(new_username text)
returns text
language plpgsql
security definer set search_path = ''
as $$
declare
  normalized_username text;
  clean_username text;
  technical_email text;
begin
  if auth.uid() is null then
    raise exception 'UsuГЎrio nГЈo autenticado.';
  end if;

  clean_username := trim(new_username);
  normalized_username := regexp_replace(
    lower(clean_username),
    '[[:space:]]+',
    '_',
    'g'
  );

  if normalized_username !~ '^[a-z0-9][a-z0-9._-]{2,23}$' then
    raise exception 'Use de 3 a 24 caracteres: letras sem acento, nГєmeros, espaГ§os, ponto, hГ­fen ou sublinhado.';
  end if;

  if exists (
    select 1
    from public.profiles
    where id <> auth.uid()
      and regexp_replace(
        lower(trim(display_name)),
        '[[:space:]]+',
        '_',
        'g'
      ) = normalized_username
  ) then
    raise exception 'Este username jГЎ estГЎ em uso.';
  end if;

  technical_email := normalized_username || '@bolasso.invalid';

  update public.profiles
  set display_name = clean_username
  where id = auth.uid();

  update auth.users
  set email = technical_email,
      raw_user_meta_data = jsonb_set(
        coalesce(raw_user_meta_data, '{}'::jsonb),
        '{display_name}',
        to_jsonb(clean_username)
      ),
      updated_at = now()
  where id = auth.uid();

  update auth.identities
  set identity_data = jsonb_set(
    identity_data,
    '{email}',
    to_jsonb(technical_email)
  ),
      updated_at = now()
  where user_id = auth.uid()
    and provider = 'email';

  return clean_username;
end;
$$;

create or replace function public.can_edit_prediction(target_match_id bigint)
returns boolean
language sql
stable
security definer set search_path = ''
as $$
  select exists (
    select 1
    from public.matches
    where id = target_match_id
      and status = 'scheduled'
      and kickoff_at - interval '5 minutes' > now()
  );
$$;

create or replace function public.get_participant_predictions(
  target_user_id uuid
)
returns table (
  match_id bigint,
  match_number smallint,
  home_team text,
  away_team text,
  home_flag text,
  away_flag text,
  kickoff_at timestamptz,
  status text,
  final_home_score smallint,
  final_away_score smallint,
  predicted_home_score smallint,
  predicted_away_score smallint,
  points smallint
)
language sql
stable
security definer set search_path = ''
as $$
  select
    m.id,
    m.match_number,
    m.home_team,
    m.away_team,
    m.home_flag,
    m.away_flag,
    m.kickoff_at,
    m.status,
    m.home_score,
    m.away_score,
    pr.home_score,
    pr.away_score,
    pr.points
  from public.matches m
  join public.predictions pr on pr.match_id = m.id
  where pr.user_id = target_user_id
    and m.kickoff_at - interval '5 minutes' <= now()
  order by m.kickoff_at desc;
$$;

create or replace function public.protect_prediction()
returns trigger
language plpgsql
security definer set search_path = ''
as $$
begin
  new.updated_at := now();

  if tg_op = 'INSERT' then
    new.points := null;
  elsif not public.is_admin() then
    new.points := old.points;
  end if;

  return new;
end;
$$;

create trigger protect_prediction_values
  before insert or update on public.predictions
  for each row execute procedure public.protect_prediction();

create or replace function public.calculate_points(
  predicted_home integer,
  predicted_away integer,
  final_home integer,
  final_away integer
)
returns integer
language plpgsql
immutable
as $$
declare
  outcome_points integer := 0;
  one_score_point integer := 0;
begin
  if predicted_home = final_home and predicted_away = final_away then
    return 7;
  end if;

  if sign(predicted_home - predicted_away) = sign(final_home - final_away) then
    outcome_points := 3;
  end if;

  if predicted_home = final_home or predicted_away = final_away then
    one_score_point := 1;
  end if;

  if outcome_points = 3 and one_score_point = 1 then
    return 5;
  end if;

  return outcome_points + one_score_point;
end;
$$;

create or replace function public.finish_match(
  target_match_id bigint,
  final_home_score integer,
  final_away_score integer
)
returns void
language plpgsql
security definer set search_path = ''
as $$
begin
  if not public.is_admin() then
    raise exception 'Apenas administradores podem publicar resultados.';
  end if;

  if final_home_score not between 0 and 99
    or final_away_score not between 0 and 99 then
    raise exception 'Placar invГЎlido.';
  end if;

  update public.matches
  set home_score = final_home_score,
      away_score = final_away_score,
      status = 'finished'
  where id = target_match_id
    and kickoff_at <= now();

  if not found then
    raise exception 'O jogo ainda nГЈo comeГ§ou ou nГЈo foi encontrado.';
  end if;

  update public.predictions
  set points = public.calculate_points(
    home_score,
    away_score,
    final_home_score,
    final_away_score
  )
  where match_id = target_match_id;
end;
$$;

create or replace function public.get_ranking()
returns table (
  user_id uuid,
  display_name text,
  total_points bigint,
  exact_scores bigint,
  predictions_count bigint
)
language sql
stable
security definer set search_path = ''
as $$
  select
    p.id as user_id,
    p.display_name,
    coalesce(sum(pr.points), 0)::bigint as total_points,
    count(*) filter (where pr.points = 7)::bigint as exact_scores,
    count(pr.match_id)::bigint as predictions_count
  from public.profiles p
  left join public.predictions pr on pr.user_id = p.id
  group by p.id, p.display_name
  order by total_points desc, exact_scores desc, p.display_name asc;
$$;

