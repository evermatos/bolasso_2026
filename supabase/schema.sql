create table public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  display_name text not null check (char_length(display_name) between 2 and 40),
  avatar_key text not null default 'classic-ball'
    check (avatar_key in (
      'classic-ball',
      'golden-cup',
      'goalkeeper',
      'football-boot',
      'supporter-horn',
      'stadium-drum',
      'goal-net',
      'brazil',
      'argentina-ten',
      'portugal-seven',
      'penguin-striker',
      'var-alien',
      'capybara-fan',
      'ball-wizard',
      'angry-referee'
    )),
  is_admin boolean not null default false,
  created_at timestamptz not null default now()
);

create table public.matches (
  id bigint generated by default as identity primary key,
  match_number smallint not null unique check (match_number between 1 and 104),
  home_team text not null,
  away_team text not null,
  home_flag text not null default '⚽',
  away_flag text not null default '⚽',
  stage text not null,
  kickoff_at timestamptz not null,
  venue text,
  status text not null default 'scheduled'
    check (status in ('scheduled', 'finished')),
  home_score smallint check (home_score between 0 and 99),
  away_score smallint check (away_score between 0 and 99),
  created_at timestamptz not null default now(),
  constraint finished_match_has_score check (
    status <> 'finished' or (home_score is not null and away_score is not null)
  )
);

create table public.predictions (
  user_id uuid not null references public.profiles(id) on delete cascade,
  match_id bigint not null references public.matches(id) on delete cascade,
  home_score smallint not null check (home_score between 0 and 99),
  away_score smallint not null check (away_score between 0 and 99),
  points smallint check (points between 0 and 7),
  updated_at timestamptz not null default now(),
  primary key (user_id, match_id)
);

create table public.ranking_movements (
  user_id uuid primary key references public.profiles(id) on delete cascade,
  previous_position integer not null check (previous_position > 0),
  current_position integer not null check (current_position > 0),
  updated_at timestamptz not null default now()
);

create table public.oracle_predictions (
  user_id uuid not null references public.profiles(id) on delete cascade,
  match_id bigint not null references public.matches(id) on delete cascade,
  pick text not null check (pick in ('home', 'draw', 'away')),
  created_at timestamptz not null default now(),
  primary key (user_id, match_id)
);

create index predictions_match_id_idx on public.predictions(match_id);
create index oracle_predictions_match_id_idx on public.oracle_predictions(match_id);
create index matches_kickoff_at_idx on public.matches(kickoff_at);
create unique index profiles_username_idx
  on public.profiles (
    regexp_replace(lower(trim(display_name)), '[[:space:]]+', '_', 'g')
  );

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer set search_path = ''
as $$
begin
  insert into public.profiles (id, display_name)
  values (
    new.id,
    coalesce(nullif(trim(new.raw_user_meta_data ->> 'display_name'), ''), 'Participante')
  );
  return new;
end;
$$;

create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

create or replace function public.is_admin()
returns boolean
language sql
stable
security definer set search_path = ''
as $$
  select coalesce(
    (select is_admin from public.profiles where id = auth.uid()),
    false
  );
$$;

create or replace function public.update_username(new_username text)
returns text
language plpgsql
security definer set search_path = ''
as $$
declare
  normalized_username text;
  clean_username text;
  technical_email text;
begin
  if auth.uid() is null then
    raise exception 'Usuário não autenticado.';
  end if;

  clean_username := trim(new_username);
  normalized_username := regexp_replace(
    lower(clean_username),
    '[[:space:]]+',
    '_',
    'g'
  );

  if normalized_username !~ '^[a-z0-9][a-z0-9._-]{2,23}$' then
    raise exception 'Use de 3 a 24 caracteres: letras sem acento, números, espaços, ponto, hífen ou sublinhado.';
  end if;

  if exists (
    select 1
    from public.profiles
    where id <> auth.uid()
      and regexp_replace(
        lower(trim(display_name)),
        '[[:space:]]+',
        '_',
        'g'
      ) = normalized_username
  ) then
    raise exception 'Este username já está em uso.';
  end if;

  technical_email := normalized_username || '@bolasso.invalid';

  update public.profiles
  set display_name = clean_username
  where id = auth.uid();

  update auth.users
  set email = technical_email,
      raw_user_meta_data = jsonb_set(
        coalesce(raw_user_meta_data, '{}'::jsonb),
        '{display_name}',
        to_jsonb(clean_username)
      ),
      updated_at = now()
  where id = auth.uid();

  update auth.identities
  set identity_data = jsonb_set(
    identity_data,
    '{email}',
    to_jsonb(technical_email)
  ),
      updated_at = now()
  where user_id = auth.uid()
    and provider = 'email';

  return clean_username;
end;
$$;

