create or replace function public.can_edit_prediction(target_match_id bigint)
returns boolean
language sql
stable
security definer set search_path = ''
as $$
  select exists (
    select 1
    from public.matches
    where id = target_match_id
      and status = 'scheduled'
      and kickoff_at - interval '5 minutes' > now()
  );
$$;

create or replace function public.get_oracle_prediction(target_match_id bigint)
returns table (
  match_id bigint,
  pick text,
  created_at timestamptz
)
language plpgsql
security definer set search_path = ''
as $$
declare
  selected_pick text;
begin
  if auth.uid() is null then
    raise exception 'Usuário não autenticado.';
  end if;

  if not exists (
    select 1 from public.matches where id = target_match_id
  ) then
    raise exception 'Jogo não encontrado.';
  end if;

  selected_pick := (
    array['home', 'draw', 'away']
  )[1 + floor(random() * 3)::integer];

  insert into public.oracle_predictions (user_id, match_id, pick)
  values (auth.uid(), target_match_id, selected_pick)
  on conflict (user_id, match_id) do nothing;

  return query
  select
    oracle_predictions.match_id,
    oracle_predictions.pick,
    oracle_predictions.created_at
  from public.oracle_predictions
  where oracle_predictions.user_id = auth.uid()
    and oracle_predictions.match_id = target_match_id;
end;
$$;

create or replace function public.get_participant_predictions(
  target_user_id uuid
)
returns table (
  match_id bigint,
  match_number smallint,
  home_team text,
  away_team text,
  home_flag text,
  away_flag text,
  kickoff_at timestamptz,
  status text,
  final_home_score smallint,
  final_away_score smallint,
  predicted_home_score smallint,
  predicted_away_score smallint,
  points smallint
)
language sql
stable
security definer set search_path = ''
as $$
  select
    m.id,
    m.match_number,
    m.home_team,
    m.away_team,
    m.home_flag,
    m.away_flag,
    m.kickoff_at,
    m.status,
    m.home_score,
    m.away_score,
    pr.home_score,
    pr.away_score,
    pr.points
  from public.matches m
  join public.predictions pr on pr.match_id = m.id
  where pr.user_id = target_user_id
    and m.kickoff_at - interval '5 minutes' <= now()
  order by m.kickoff_at desc;
$$;

create or replace function public.protect_prediction()
returns trigger
language plpgsql
security definer set search_path = ''
as $$
begin
  new.updated_at := now();

  if tg_op = 'INSERT' then
    new.points := null;
  elsif not public.is_admin() then
    new.points := old.points;
  end if;

  return new;
end;
$$;

create trigger protect_prediction_values
  before insert or update on public.predictions
  for each row execute procedure public.protect_prediction();

create or replace function public.calculate_points(
  predicted_home integer,
  predicted_away integer,
  final_home integer,
  final_away integer
)
returns integer
language plpgsql
immutable
as $$
declare
  outcome_points integer := 0;
  one_score_point integer := 0;
begin
  if predicted_home = final_home and predicted_away = final_away then
    return 7;
  end if;

  if sign(predicted_home - predicted_away) = sign(final_home - final_away) then
    outcome_points := 3;
  end if;

  if predicted_home = final_home or predicted_away = final_away then
    one_score_point := 1;
  end if;

  if outcome_points = 3 and one_score_point = 1 then
    return 5;
  end if;

  return outcome_points + one_score_point;
end;
$$;

create or replace function public.refresh_ranking_movements()
returns void
language plpgsql
security definer set search_path = ''
as $$
begin
  with totals as (
    select
      p.id as user_id,
      p.display_name,
      coalesce(sum(pr.points), 0)::bigint as total_points,
      count(*) filter (where pr.points = 7)::bigint as exact_scores,
      count(*) filter (where pr.points = 5)::bigint as five_point_scores,
      count(*) filter (where pr.points = 3)::bigint as three_point_scores,
      count(*) filter (where pr.points = 1)::bigint as one_point_scores
    from public.profiles p
    left join public.predictions pr on pr.user_id = p.id
    group by p.id, p.display_name
  ),
  ranked as (
    select
      user_id,
      rank() over (
        order by
          total_points desc,
          exact_scores desc,
          five_point_scores desc,
          three_point_scores desc,
          one_point_scores desc
      )::integer as ranking_position
    from totals
  )
  insert into public.ranking_movements (
    user_id,
    previous_position,
    current_position,
    updated_at
  )
  select
    ranked.user_id,
    coalesce(movements.current_position, ranked.ranking_position),
    ranked.ranking_position,
    now()
  from ranked
  left join public.ranking_movements movements
    on movements.user_id = ranked.user_id
  on conflict (user_id) do update
  set previous_position = public.ranking_movements.current_position,
      current_position = excluded.current_position,
      updated_at = excluded.updated_at;
end;
$$;

create or replace function public.finish_match(
  target_match_id bigint,
  final_home_score integer,
  final_away_score integer
)
returns void
language plpgsql
security definer set search_path = ''
as $$
begin
  if not public.is_admin() then
    raise exception 'Apenas administradores podem publicar resultados.';
  end if;

  if final_home_score not between 0 and 99
    or final_away_score not between 0 and 99 then
    raise exception 'Placar inválido.';
  end if;

  update public.matches
  set home_score = final_home_score,
      away_score = final_away_score,
      status = 'finished'
  where id = target_match_id
    and kickoff_at <= now();

  if not found then
    raise exception 'O jogo ainda não começou ou não foi encontrado.';
  end if;

  update public.predictions
  set points = public.calculate_points(
    home_score,
    away_score,
    final_home_score,
    final_away_score
  )
  where match_id = target_match_id;

  perform public.refresh_ranking_movements();
end;
$$;

create or replace function public.get_ranking()
returns table (
  user_id uuid,
  display_name text,
  avatar_key text,
  ranking_position integer,
  position_change integer,
  is_tied boolean,
  total_points bigint,
  exact_scores bigint,
  five_point_scores bigint,
  three_point_scores bigint,
  one_point_scores bigint,
  predictions_count bigint
)
language sql
stable
security definer set search_path = ''
as $$
  with totals as (
    select
      p.id as user_id,
      p.display_name,
      p.avatar_key,
      coalesce(sum(pr.points), 0)::bigint as total_points,
      count(*) filter (where pr.points = 7)::bigint as exact_scores,
      count(*) filter (where pr.points = 5)::bigint as five_point_scores,
      count(*) filter (where pr.points = 3)::bigint as three_point_scores,
      count(*) filter (where pr.points = 1)::bigint as one_point_scores,
      count(pr.match_id)::bigint as predictions_count
    from public.profiles p
    left join public.predictions pr on pr.user_id = p.id
    group by p.id, p.display_name, p.avatar_key
  ),
  ranked as (
    select
      totals.*,
      rank() over (
        order by
          total_points desc,
          exact_scores desc,
          five_point_scores desc,
          three_point_scores desc,
          one_point_scores desc
      )::integer as ranking_position,
      count(*) over (
        partition by
          total_points,
          exact_scores,
          five_point_scores,
          three_point_scores,
          one_point_scores
      ) > 1 as is_tied
    from totals
  )
  select
    ranked.user_id,
    ranked.display_name,
    ranked.avatar_key,
    ranked.ranking_position,
    coalesce(
      movements.previous_position - movements.current_position,
      0
    )::integer as position_change,
    ranked.is_tied,
    ranked.total_points,
    ranked.exact_scores,
    ranked.five_point_scores,
    ranked.three_point_scores,
    ranked.one_point_scores,
    ranked.predictions_count
  from ranked
  left join public.ranking_movements movements
    on movements.user_id = ranked.user_id
  order by ranked.ranking_position, ranked.display_name asc;
$$;

